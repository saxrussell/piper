# Basic string stuff

# first, define a variable that contains a string value
greeting_string = "Greetings from your local Python interpreter!"

'''
now that our string is in memory, do something with it. calling the 
print() function on it will write the string to the operating system's 
standard output (a.k.a. "stdout") channel...usually the terminal window 
where the python program was executed.
'''
print(greeting_string)

'''
the above demonstrates a key concept in programming...objects and
functions. the greeting_string variable is an object. objects are kind
of like nouns. functions perform work by taking inputs and producing
outputs. in the case above, we use the object greeting_string as an
input to the print() function which then produces an output from the
input object.
'''